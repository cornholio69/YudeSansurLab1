package edu.fiu.pagen20;

public class Location {

	private int Floor;
	private int Unit;
	private int Wing;

	public void Updates() {}

	/**
	 * @return the floor
	 */
	public int getFloor() {
		return Floor;
	}

	/**
	 * @param floor the floor to set
	 */
	public void setFloor(int floor) {
		Floor = floor;
	}

	/**
	 * @return the unit
	 */
	public int getUnit() {
		return Unit;
	}

	/**
	 * @param unit the unit to set
	 */
	public void setUnit(int unit) {
		Unit = unit;
	}

	/**
	 * @return the wing
	 */
	public int getWing() {
		return Wing;
	}

	/**
	 * @param wing the wing to set
	 */
	public void setWing(int wing) {
		Wing = wing;
	}

}