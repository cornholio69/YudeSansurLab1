package edu.fiu.pagen20;

public class Status {

	private int Online;
	private int Offline;
	private int Busy;
	private int Onbreak;


	/**
	 * @return the online
	 */
	public int getOnline() {
		return Online;
	}

	/**
	 * @param online the online to set
	 */
	public void setOnline(int online) {
		Online = online;
	}

	/**
	 * @return the offline
	 */
	public int getOffline() {
		return Offline;
	}

	/**
	 * @param offline the offline to set
	 */
	public void setOffline(int offline) {
		Offline = offline;
	}

	/**
	 * @return the busy
	 */
	public int getBusy() {
		return Busy;
	}

	/**
	 * @param busy the busy to set
	 */
	public void setBusy(int busy) {
		Busy = busy;
	}

	/**
	 * @return the onbreak
	 */
	public int getOnbreak() {
		return Onbreak;
	}

	/**
	 * @param onbreak the onbreak to set
	 */
	public void setOnbreak(int onbreak) {
		Onbreak = onbreak;
	}

}