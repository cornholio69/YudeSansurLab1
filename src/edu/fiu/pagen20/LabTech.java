package edu.fiu.pagen20;

public class LabTech extends Users {

	private int Name;
	private int LabLocation;
	/**
	 * @return the name
	 */
	public int getName() {
		return Name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(int name) {
		Name = name;
	}
	/**
	 * @return the labLocation
	 */
	public int getLabLocation() {
		return LabLocation;
	}
	/**
	 * @param labLocation the labLocation to set
	 */
	public void setLabLocation(int labLocation) {
		LabLocation = labLocation;
	}

}